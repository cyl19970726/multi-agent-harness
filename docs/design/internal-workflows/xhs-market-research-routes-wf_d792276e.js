const research = await parallel(dims.map(d => () =>
  agent(
    `你是小红书文创/文旅营销研究员。${d.q}\n\n【真实数据简报】\n${DATA_BRIEF}\n\n要求:用WebSearch补充2025-2026最新真实案例(给出可核实的案例名,不要编造数据),与数据简报结合。输出中文,400-700字,结构化(要点+对揭阳古城的具体建议)。这是给团队看的调研结论。`,
    {label:d.label, phase:'Research'}
  ).then(text => ({dim:d.key, text}))
));

phase('Synthesize');

